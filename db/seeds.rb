def seed_plans
  plan = ReadingPlan.where(plan_name: '1 Year Chronological')
  return if plan.present?

  plan = ReadingPlan.create!(
    plan_name: '1 Year Chronological',
    description: 'One year chronological reading plan',
    days: 365
  )
  html_doc = Nokogiri::HTML(PLAN1)
  html_doc.css('a').each_with_index do |node, i|
    plan.reading_plan_details.create!(
      plan: node.children.first.text,
      detailed_plan: node.attributes['href'].value.split('?q=').last,
      day: i+1
    )
  end
end

seed_plans
